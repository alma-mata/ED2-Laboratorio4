void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){

	if (carrera_start != 2 && GPIO_Pin != BTN_start_Pin)
			return;
	if (GPIO_Pin == BTN_counter1_Pin && winner == 0){
		if (jugador1 == 0)		// Si el contador es 0
		{
			jugador1 = 0x01;	// Inicializarlo con 1
		}
		else{					// De lo contrario
			jugador1 = ((jugador1 << 1) & 0x0F);	// Correr los bits 1 posición
			if (jugador1 == 0x08)	// Si es 0b1000
			{
				winner = 1;			// Declararlo como ganador
			}
		}
	}
	else if (GPIO_Pin == BTN_counter2_Pin && winner == 0){
		if (jugador2 == 0)		// Si el contador es 0
		{
			jugador2 = 0x01;	// Inicializarlo con 1
		}
		else{					// De lo contrario
			jugador2 = ((jugador2 << 1) & 0x0F);	// Correr los bits 1 posición
			if (jugador2 == 0x08)	// Si es 0b1000
			{
				winner = 2;			// Declararlo como ganador
			}
		}
	}
	else if (GPIO_Pin == BTN_start_Pin && winner != 0){
		winner = 0;
		jugador1 = 0;
		jugador2 = 0;

		countdown = 5;
		carrera_start = 1;  // Inicia cuenta regresiva

		HAL_TIM_Base_Start_IT(&htim2);  // Se activa interrupcion del timer2
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim->Instance == TIM2){
		if(carrera_start == 1)
		{
			if(countdown == 0){
				carrera_start = 2;
				HAL_TIM_Base_Stop_IT(&htim2);
			}
			else{
				countdown--;
			}
		}
	}
}
